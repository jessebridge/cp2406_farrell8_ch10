public class Horse {
    public String HorseName;
    public String HorseColour;
    public int BirthYear;

    public String getHorseName(){
        return HorseName;
    }

    public String getHorseColour() {
        return HorseColour;
    }
    public int getBirthYear(){
        return BirthYear;
    }

    public void setHorseName(String n){
        HorseName = n;
    }
    public void setHorseColour(String c){
        HorseColour = c;
    }
    public void setBirthYear(int b){
        BirthYear = b;
    }
}
